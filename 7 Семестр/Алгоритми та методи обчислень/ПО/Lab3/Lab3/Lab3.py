def LagrangeFunction(list_x, list_y, value):
    z=0
    for j in range(len(list_y)):
        p1=1; p2=1
        for i in range(len(list_x)):
            if i==j:
                p1=p1*1; p2=p2*1   
            else: 
                p1=p1*(value-list_x[i])
                p2=p2*(list_x[j]-list_x[i])
        z=z+list_y[j]*p1/p2
    return z
